package algorithm;

public class PalindromeChecker {

    public boolean isPalindrome(int num) {
        int reversed = 0;
        int original = num;
        while (num != 0) {
            int digit = num % 10;
            reversed = reversed * 10 + digit;
            num /= 10;
        }
        return original == reversed;
    }
}
